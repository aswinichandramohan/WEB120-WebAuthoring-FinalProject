    <p>There are plenty of effects and features that can add to the site, and one of them is the scrolling parallax effect where the background content (i.e. an image) is moved at a different speed than the foreground content while scrolling. parallax scrolling is nowadays used in a wide variety of websites, including portfolios, corporate sites, landing pages, and so on. According to Sufyan bin Uzayr of envato.com, “parallax can be implemented in various ways: Raster Parallax, which is mostly used to give an optical illusion of movement. Repeat Pattern, that uses scrolling displays over a repeated background. Layered Parallax, where the foreground and background objects move at a different pace” (Uzayr).</p>

	<p>The Parallax Scroll Effect jQuery plugin helps us implement the Parallax Scrolling Effect on elements (typically background images) as we scroll down or up the webpage. 
According to Shrinivas of techsini.com, “Steller.js is a jquery parallax plugins to add parallax effect to an HTML element. Just initialize the steller script and specify the ratio data attribute to any element for which we want to apply the parallax effect. Steller.js is a good parallax plugin which is butter smooth and very easy to implement” (Shrinivas).</p>

	<p>SEO (Search Engine Optimization) is another factor that is often overlooked in parallax scrolling. As per Sufyan bin Uzayr of envato.com, “At times, it is claimed that parallax is disastrous for our website’s SEO. While this claim is too far-fetched to be true, we still need to tread the SEO road with caution, especially if you are using parallax scrolling on your website” (Uzayr).</p>



