    <p>Flexbox is a layout model that allows elements to align and distribute space within a container. Using flexible widths and heights, elements can be aligned to fill a space or distribute space between elements, which makes it a great tool to use for responsive websites.These are the uses that flexbox was designed for. </p> 

	<p>According to Scott Vandehey of Space Ninja, ”Flexbox is a layout mode added in CSS3 to replace hacky float and table layouts. It comes with a clever set of defaults that make it easy to create complex layouts with relatively little code. It’s stable, easy-to-use, and well-supported” (Vandehey).</p>

	<p>These here are drawbacks of using flexbox are the performance issues and browser compatibility. Nihkil Krishnan of toobler.com stated that, ”Flexbox layout is completely supported by popular browsers like Chrome, Firefox, Safari, etc which are always  up-to-date and the older versions don’t support Flexbox. It is not supported by most of the browsers and it has been reported that the use of flexbox increases the page loading time for larger projects” (Krishnan). </p>

	<p>Flexbox is the future of grid design. Billy Williams of distinction.co.uk stated that, “In my view, it is as close to a 'baked in' framework (such as Bootstrap) as base CSS has come. As such several popular frameworks are adopting Flexbox for their future releases, with Bootstrap 4 and Foundation 6 both including a Flexbox grid framework. This future is ridiculously exciting for front end developers, as it means we can concentrate on creating exciting and expansive designs which naturally adapt to screen sizes with minimal effort, rather than having to concentrate our efforts on centering and changing DOM (Document Object Model) elements using Javascript or jQuery” (Williams).</p>

